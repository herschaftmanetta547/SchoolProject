#include <iostream>
using namespace std;

int main() {
    int x = 5;
    int y = 3;
    
    cout << "The value of x is: " << x << endl;
    cout << "The value of y is: " << y << endl;
    
    if (x > y) {
        cout << "x is greater than y." << endl;
    } else {
        cout << "y is greater than x." << endl;
    }
    
    return 0;
}
